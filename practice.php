<?php
class doMath {
    public function add ($number1, $number2) {
        return $number1 + $number2;
    }
    
    public function subtract ($number1, $number2) {
        return $number1 - $number2;
    }
    
    public function multiply ($number1, $number2) {
        return $number1 * $number2;
    }
}